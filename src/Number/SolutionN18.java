package Number;

import java.util.Scanner;

public class SolutionN18 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        // System.out.println(a + b);
        System.out.println("a = " + a);
        System.out.println("b = " + b);

        // a + b를 "a = " + a, "b = " + b 형태로 수정
    }
}